package DigitalClock1;

public class DigitalClock {
    public static void main(String[] args) {
        for (int i = 0; i <=24 ; i++) {
            for (int j = 0; j <=59 ; j++) {
                System.out.println(i+":"+j);
            }
            System.out.println();
            
        }


    }
}
